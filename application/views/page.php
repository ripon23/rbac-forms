        <h2>Overview</h2>
        <p><strong>Portable Health Clinic (PHC)</strong> aims to build an affordable, usable and sustainable preventive healthcare system for unreached people. Portable Health Clinic has four major modules (1) Health Clinic box with different medical sensors (2) An empowered female healthcare entrepreneur (we call her Grameen Healthcare Lady) (3) GramHealth software tool to store and manage healtcare data (4) A doctor call center in urban areas to provide suggestions to the patients by using video conferencing tool.</p>
        
        <p><strong>Brief History:</strong></p>
        <p>The project was initiated by Dr. Ashir Ahmed in 2008. JST and Fukuoka IST funded the project for the survey and design a booth shaped clinic. In 2009, the health clinic was redesigned to make it portable and modular suggested by Professor Muhammad Yunus and Professor Hiroto Yasuura. In 2010, we served 600 families in Ekhlaspur to develop the GramHealth Database. In 2011, we carried out our portable health clinic trials in Basundia, Panchagarh and Rampal by using urban doctors. Square Hospitals collaborated with us to provide online doctors. In 2012, the project became larger and focused on non-communicable diseases by the supervision of Dr. Nakashima in Kyushu University. Dr. Kuroda from NICT added BAN (Body Area Network) in the medical sensors for easier inputs of medical data. In 2013, we employed two professional nurses graduated from Grameen Caledonian College of Nursing to test the model the activities of a Grameen Healthcare Lady.
        </p>
        
